import Foundation

public class MathUtility {
    
    public let pi = 3.142345
    
    public init() {}
    
   public func add(a: Int, b: Int) -> Int {
        
        return a + b
    }
    
   public func substract(a: Int, b: Int) -> Int {
        
        return a - b
    }
    
    public func multiply(a: Int, b: Int) -> Int {
        
        return a * b
    }
    
   public func divide(a: Int, b: Int) -> Int {
        
        return a / b
    }
}
